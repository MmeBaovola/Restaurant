package models;

import dbTable.DbTablePostgres;
import java.sql.Date;

public class Inventaire extends DbTablePostgres {
    int id;
    Date date;

    public Inventaire() {
    }

    public Inventaire(int id, Date date) {
        this.id = id;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

}
